						<h2>Collecting the Bamboo</h2>
						<div class="popcontxt">
              <p>Shifu was collecting one-year-old bamboo plants. He walked in water, and investigated. With merely one glimpse, he could tell which bamboo made the best material. He studied the direction of the bamboo hair in order to judge which plants were bitter bamboo, and which were sweet. Only the bitter plants are suitable for making bamboo woven ware. “Tall and straight, with long joints, that makes the best material” said Master Zhang. Some of the bamboo plants are crooked, their lives are determined by changes in direction. Only the strong and upright are true bamboo plants that can bravely face what is presented to them in life. </p>
              <p>The cutters aimed carefully and cut the bamboo from the roots. Then they cut off the branches lengthways, and separated the bamboo into several segments using a hacksaw. Finally, they carried them down the mountain. This is usually a job for men. </p>
