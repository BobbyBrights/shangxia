                  <p>Master Zhang created a new weaving technique for "SHANG XIA"'s white porcelain tea set "Bridge". The technique is called Bamboo covered Porcelain, an innovative and creative process based on the traditional bamboo over porcelain technique. Around the ceramic body, the thin bamboo strips are softly woven following the shape of the tea ware, melding seamlessly one material into the other.</p>
                  <p>Unlike other bamboo woven wares, bamboo covered porcelain is a delicate work. The choice of bamboo material is extremely rigid. The bamboo piece must be over 66 cm long between two ridges, lack marks or scratches of any kind, and be at least two years of age. The traditional expression used to describe superior quality bamboo states "vertical bamboo strips as thin as silk, horizontal strips stretched tight as a strand of hair. The quality of the set is such that 50 kg of bamboo can produce only 400 grams of finished bamboo strips, the same price as silver.</p>
                  <p>According to tradition, bamboo covered porcelain uses a single base slice and a single strand to weave the pattern. But Master Zhang discovered that using double strands creates a totally different pattern and final result.</p>
                  <p>Master Zhang points out, "The double cross strands technique creates a 3D effect. The strands I use are only 0.4 mm wide, and the slices are even thinner." The innovation turns the craftsmanship into an art form.</p>
