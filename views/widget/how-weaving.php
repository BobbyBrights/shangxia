						<h2>Separating <br/>the Bamboo</h2>
						<div class="popcontxt">
              <p>The first step was to separate bamboo into blocks, as quickly and as accurately as possible, when the bamboo was still fresh and tender. The bamboo stalk is split into equally thick segments and these in turn are split into strips. Taking a smooth strip in the palm of the left hand, holding the strip with the thumb and the index finger one centimeter away from the end, the cutter uses her right hand, a third of the way down the slice the cutter cuts into the bamboo, slicing toward herself. </p>
              <p>After hundreds of thousands of times of practice, a woman took two movements to complete the task. She cut a block, swung it up and down, then one bamboo slice fell off. She then cut again and swung, another slice off, and then another. All of the bamboo was sliced up in this way. Bamboo woven wares only require the first four layers of slices, sometimes only the first layer, since they have the best sheen.  </p>
              <p>Finally, the slices needed to be scraped again and again, until they were transparent, thinner than silk, less than 1mm.</p>
